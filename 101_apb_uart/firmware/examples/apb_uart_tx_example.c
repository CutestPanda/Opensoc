#include "../apb_uart.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define APB_UART_BASEADDR 0x40000000 // APB-UART外设基地址

////////////////////////////////////////////////////////////////////////////////////////////////////////////

static ApbUART uart; // APB-UART外设结构体

////////////////////////////////////////////////////////////////////////////////////////////////////////////

void apb_uart_tx_example(void){
	apb_uart_init(&uart, APB_UART_BASEADDR); // 初始化APB-UART
	
	// 发送字符串
	uart_printf(&uart, "hello, world!\n\r");
	
	while(1);
}
